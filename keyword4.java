//     Keywords in Java
//     Keywords are reserved words in Java that have special meanings. They cannot be used as variable names, class names, or identifiers.
    
//     Java has 50 keywords (as of Java 17). Here's a categorized list:
    
//     üîπ Control Flow Keywords
//     Keyword	Use
//     if	Conditional statement
//     else	Executes when if condition is false
//     switch	Multi-branch selection
//     case	Defines individual cases in switch
//     default	Executes when no case matches
//     while	Loop with condition check at start
//     do	Loop with condition check at end
//     for	Loop with initialization, condition, update
//     break	Exits loop or switch
//     continue	Skips current loop iteration
//     return	Exits from method and returns value
    
//     üîπ Data Types
//     Keyword	Type
//     int	Integer
//     float	Decimal
//     double	Big decimal
//     char	Character
//     boolean	true/false
//     byte	Small integer
//     short	Smaller integer
//     long	Larger integer
//     void	No return type
    
//     üîπ Object-Oriented Keywords
//     Keyword	Use
//     class	Declares a class
//     interface	Declares an interface
//     extends	Inherits a class
//     implements	Implements an interface
//     new	Creates an object
//     this	Refers to current object
//     super	Refers to parent class
    
//     üîπ Access Modifiers
//     Keyword	Access Control
//     public	Accessible everywhere
//     private	Accessible within class only
//     protected	Accessible within package + subclass
//     static	Shared by all objects
//     final	Cannot be changed/overridden
//     abstract	Incomplete; must be extended
    
//     üîπ Exception Handling
//     Keyword	Use
//     try	Code to try
//     catch	Catches exception
//     finally	Executes no matter what
//     throw	Throws an exception
//     throws	Declares possible exceptions
    
//     üîπ Other Important Keywords
//     Keyword	Use
//     import	Includes packages/classes
//     package	Defines a package
//     instanceof	Checks object type
//     enum	Declares a fixed set of constants
//     synchronized	Thread-safe access
//     volatile	Prevents caching of variable
//     transient	Skip during serialization
//     native	Links to non-Java code
//     strictfp	Enforces floating point standard
//     assert	For debugging
    
//     ‚ùå Reserved but Not Used
//     Keyword
//     goto
//     const
